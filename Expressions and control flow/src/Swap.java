public class Swap {

    public static void main(String[] args) {

        //int a, b, temp;
        int a = 123;
        int b = 526;
        int temp;
        System.out.println("Before swapping : a, b = " + a + ", " + b);
        temp = a;
        a = b;
        b = temp;
        System.out.println(a);
        System.out.println(b);
    }


}
