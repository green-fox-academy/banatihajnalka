public class TwoNumbers {
    public static void main(String[] args) {
        int a = 22;
        int b = 13;
        System.out.println(b + a);
        System.out.println(b - a);
        System.out.println(a * b);
        System.out.println(22. / 13);
        System.out.println(a / b);
        System.out.println(a % b);
    }
}
